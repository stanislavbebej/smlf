Nasleduje vynásobenie systému funkcií navzájom. Je potrebné na nájdenie skupinových implikantov zúčastnených funkcií.
Funkcie sa násobia tak, že sa vynásobí prvé pole karnaughovej mapy prvej funkcie s prvým poľom karnaughovej mapy druhej
funkcie atď. Výsledkom je nová funkcia s takým istým počtom premenných, ako funkcie, z ktorých vznikla. Táto má tiež
identickú veľkosť karnaughovej mapy, ktorej hodnoty sú práve výsledky súčinu funkcií. Pole má hodnotu 1, ak obidve
funkcie, z ktorých nová funkcia vznikla, mali v príslušnom poli tabuľky hodnotu 1. Inak je tam hodnota 0.
<br>
Dole môžete vidieť Vami zvolené funkcie, ako sa postupne medzi sebou roznásobia vytvárajúc nové funkcie. Pre ne treba
tiež nájsť množinu prostých implikantov ako bolo urobené pre zadané funkcie v predchádzajúcom kroku.
<br>
